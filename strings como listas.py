#strings como listas
my_string="Curso de código facilito"
print(my_string[10])
#recordar que los espacios en blanco cuenta como caracteres 
# y se empieza a contar desde la posicion 0.
print(my_string[0:10])
# De esta forma se establece el rango de caracteres que se van a imprimir
print(my_string[0:10:3])
#De esra forma se realizan saltos progresivos. 
# Donde el tercer número indica la cantidad de saltos 
# que dará entre la línea de caracteres